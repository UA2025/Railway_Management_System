    private void ShowPassengers()
    {
        Admin ad= Admin.getinstance();
        jLabel1.setText(ad.getUsername());
        try {
            Passenger Sch= new Passenger();
            ResultSet res=Sch.Pass();
            java.sql.ResultSetMetaData rsmd = res.getMetaData();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int cols = rsmd.getColumnCount();
            String[] colname = new String[cols];
            for (int i = 0; i < cols; i++) {
                colname[i] = rsmd.getColumnName(i + 1);
            }
            model.setColumnIdentifiers(colname);
            String id, trid, date,time,status,phone;
            while(res.next())
            {
                id = res.getString(1);
                trid = res.getString(2);
                date = res.getString(3);
                time = res.getString(4);
                status = res.getString(5);
                phone = res.getString(6);
                //year=res.getString(6);
                
                String[] row = {id, trid, date,time,status,phone};
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ScheduleGUI1.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
    }
